class User {
  final String uid;
  final String firstName;
  final String lastName;
  final String email;
  final String phone;
  final int gender;
  final DateTime birthday;
  final String address;
  final String avatar;

  User(
      {this.uid,
      this.firstName,
      this.lastName,
      this.email,
      this.phone,
      this.gender,
      this.birthday,
      this.address,
      this.avatar});
}
